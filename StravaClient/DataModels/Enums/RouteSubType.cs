namespace StravaClient
{
    /// <summary>
    /// Provides enumeration over the route sub types.
    /// </summary>
    /// <remarks>
    /// This route's sub-type (1 for road, 2 for mountain bike, 3 for cross, 4 for trail, 5 for mixed).
    /// </remarks>
    public enum RouteSubType
    {
        /// <summary>
        /// Road
        /// </summary>
        Road,

        /// <summary>
        /// MountainBike
        /// </summary>
        MountainBike,

        /// <summary>
        /// Cross
        /// </summary>
        Cross,

        /// <summary>
        /// Trail
        /// </summary>
        Trail,

        /// <summary>
        /// Mixed
        /// </summary>
        Mixed
    }

}